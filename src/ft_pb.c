#include "../inc/push_swap.h"

void	ft_pb(ps_list *ps)
{
	int	i;

	i = ps->nb;
	ps->nb += 1;
	ps->na -= 1;
	while (i > 0)
	{
		ft_swap(&ps->sb[i], &ps->sb[i - 1]);
		i--;
	}
	ps->sb[0] = ps->sa[0];
	while (i <= ps->na - 1)
	{
		ps->sa[i] = ps->sa[i + 1];
		i++;
	}
	ft_putstr_fd("pb\n", 1);
}
