#include "../inc/push_swap.h"

void	ft_rrb(ps_list *ps)
{
	int	i;

	i = ps->nb - 1;
	while(i - 1 >= 0)
	{
		ft_swap(&ps->sb[i], &ps->sb[i - 1]);
		i--;
	}
	ft_putstr_fd("rrb\n", 1);
}
