#include "../inc/push_swap.h"

void	ft_ra(ps_list *ps)
{
	int	i;

	i = 0;
	while (i < ps->na - 1)
	{
		ft_swap(&ps->sa[i], &ps->sa[i + 1]);
		i++;
	}
	ft_putstr_fd("ra\n", 1);
}
