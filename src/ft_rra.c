#include "../inc/push_swap.h"

void	ft_rra(ps_list *ps)
{
	int	i;

	i = ps->na - 1;
	while (i > 0)
	{
		ft_swap(&ps->sa[i], &ps->sa[i - 1]);
		i--;
	}
	ft_putstr_fd("rra\n", 1);
}
