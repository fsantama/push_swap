#include "../inc/push_swap.h"

void	ft_3args(ps_list *ps, int *n)
{
	if (n[0] > n[1] && n[0] < n[2] && n[1] < n[2])
		ft_sa(n);
	else if (n[0] > n[1] && n[0] > n[2] && n[1] > n[2])
	{
		ft_ra(ps);
		ft_sa(n);
	}
	else if (n[0] < n[1] && n[0] < n[2] && n[1] > n[2])
	{
		ft_rra(ps);
		ft_sa(n);
	}
	else if (n[0] < n[1] && n[0] > n[2] && n[1] > n[2])
		ft_rra(ps);
	else if (n[0] > n[1] && n[0] > n[2] && n[1] < n[2])
		ft_ra(ps);
}
