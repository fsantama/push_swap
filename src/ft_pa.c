#include "../inc/push_swap.h"

void	ft_pa(ps_list *ps)
{
	int	i;

	i = ps->na;
	ps->na += 1;
	ps->nb -= 1;
	while (i > 0)
	{
		ft_swap(&ps->sa[i], &ps->sa[i - 1]);
		i--;
	}
	ps->sa[0] = ps->sb[0];
	while (i <= ps->nb)
	{
		ps->sb[i] = ps->sb[i + 1];
		i++;
	}
	ft_putstr_fd("pa\n", 1);
}
